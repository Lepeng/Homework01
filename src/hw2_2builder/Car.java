package hw2_2builder;

public class Car {

	private String car_body;
	private String car_number;
	private String car_ability;
	
	public void setCarBody(String car_body) {
		this.car_body = car_body;
	}
	
	public String getCarBody() {
		return car_body;
	}
	
	public void setCarNumber(String car_number) {
		this.car_number = car_number;
	}
	
	public String getCarNumber() {
		return car_number;
	}
	
	public void setCarAbility(String car_ability) {
		this.car_ability = car_ability;
	}
	
	public String getCarAbility() {
		return car_ability;
	}
	
}
